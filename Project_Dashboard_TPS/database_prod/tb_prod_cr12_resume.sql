CREATE TABLE tb_prod_cr12_resume (
    id INT(11) NOT NULL AUTO_INCREMENT,
    production_group VARCHAR(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_uca1400_ai_ci NULL DEFAULT NULL,
    line_prod VARCHAR(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_uca1400_ai_ci NULL DEFAULT NULL,
    name_part VARCHAR(150) CHARACTER SET utf8mb4 COLLATE utf8mb4_uca1400_ai_ci NULL DEFAULT NULL,
    actual_pcs INT(11) NULL DEFAULT 0,
    loading_time DECIMAL(10,2) NULL DEFAULT NULL,
    cycle_time DECIMAL(10,2) NULL DEFAULT NULL,
    date_prod_start DATE NULL DEFAULT NULL,
    date_prod_stop DATE NULL DEFAULT NULL,
    time_prod_start TIME NULL DEFAULT NULL,
    time_prod_stop TIME NULL DEFAULT NULL,
    shift_prod VARCHAR(50) CHARACTER SET utf8mb4 COLLATE utf8mb4_uca1400_ai_ci NULL DEFAULT NULL,
    total_prod_pcs INT(11) NULL DEFAULT 0,
    down_time DECIMAL(10,2) NULL DEFAULT 0.00,
    note_down_time TEXT CHARACTER SET utf8mb4 COLLATE utf8mb4_uca1400_ai_ci NULL DEFAULT NULL,
    manpower INT(11) NULL DEFAULT 0,
    status ENUM('START', 'STOP', 'NORMAL', 'ABNORMAL') CHARACTER SET utf8mb4 COLLATE utf8mb4_uca1400_ai_ci NULL DEFAULT 'NORMAL',
    created_at TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP(),
    updated_at TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP() ON UPDATE CURRENT_TIMESTAMP(),
    PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_uca1400_ai_ci;
